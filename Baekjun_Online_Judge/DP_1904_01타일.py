n = int(input())

count = [0] * (n + 1000001)
count[1], count[2] = 1, 2

'''
2진수니까 뒤에다가 더해야함
1, 00, 11 을 더하는 경우 중, 11의 경우, 1을 더하는 것 내부에 포함됨.
'''
for i in range(3, n + 1):
    count[i] = (count[i - 1] + count[i - 2]) % 15746
print(count[n])




